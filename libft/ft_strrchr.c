#include <string.h>

char	*ft_strrchr(char *s, int c)
{
	int	q;

	q = 0;
	while (s[q] != '\0')
		q++;
	while (q > 0)
	{
		if (s[q] == (char)c)
			return (&s[q]);
		q--;
	}
	return (NULL);
}
