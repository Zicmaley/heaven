int	ft_strequ(char const *s1, char const *s2)
{
	int q;

	q = 0;
	if (s1 && s2)
	{
		while (s1[q] || s2[q])
		{
			if (s1[q] != s2[q])
				return (0);
			q++;
		}
		return (1);
	}
	return (0);
}
