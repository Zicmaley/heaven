#include <string.h>

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	int		q;
	size_t	i;

	q = 0;
	i = 0;
	while (s1[q] != '\0')
		q++;
	while (s2[i] != '\0' && i < n)
	{
		s1[q] = s2[i];
		i++;
		q++;
	}
	s1[q] = '\0';
	return (s1);
}
