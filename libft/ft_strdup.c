#include <stdlib.h>

char	*ft_strdup(char *s1)
{
	char	*s2;
	int		q;

	q = 0;
	while (s1[q] != '\0')
		q++;
	s2 = (char *)malloc(sizeof(char) * (q + 1));
	q = 0;
	if (!s2)
		return (0);
	while (s1[q] != '\0')
	{
		s2[q] = s1[q];
		q++;
	}
	s2[q] = '\0';
	return (s2);
}
