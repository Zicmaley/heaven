#include <unistd.h>

void	putcharc(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		write(1, "-", 1);
		write(1, "2", 1);
		n = 147483648;
	}
	if (n < 0)
	{
		putcharc('-');
		n *= -1;
	}
	if (n > 9)
		ft_putnbr(n / 10);
	putcharc((n % 10) + 48);
}
