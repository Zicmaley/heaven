#include <string.h>

char	*ft_strcat(char *s1, char *s2)
{
	int q;
	int i;

	i = 0;
	q = 0;
	while (s1[q] != '\0')
		q++;
	while (s2[i] != '\0')
	{
		s1[q] = s2[i];
		i++;
		q++;
	}
	s1[q] = '\0';
	return (s1);
}
