#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	char *ms;

	ms = (char *)s;
	while (*ms != c)
	{
		if (*ms == '\0')
		{
			return (NULL);
		}
		ms++;
	}
	return (ms);
}
